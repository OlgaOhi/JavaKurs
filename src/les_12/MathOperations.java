package les_12;

public class MathOperations {
    public static void main(String[] args) {
        int var1 = 21;
        int var2 = 7;

        int result;

        result = var1 + var2;

        System.out.println("result: " + result);

        result = var1 - var2;
        System.out.println("result. " + result);

        result = var1 * var2;
        System.out.println("result: " + result);

        result = var1 / var2;
        System.out.println("result: " + result);

        result = var2 % var1;
        System.out.println("result: " + result);

        System.out.println("5 / 10 = " + (5 / 10));
        System.out.println("5 % 10 = " + (5 % 10));

        System.out.println();


    }
}
